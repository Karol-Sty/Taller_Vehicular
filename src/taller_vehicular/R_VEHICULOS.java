    private int calcularCostoManoDeObra(int horasTrabajadas, String tipoVehiculo) {
        int costoPorHora = 0;

        if ("Automóvil".equals(tipoVehiculo)) {
            costoPorHora = COSTO_AUTOMOVIL;
        } else if ("Camioneta".equals(tipoVehiculo)) {
            costoPorHora = COSTO_CAMIONETA;
        }

        return horasTrabajadas * costoPorHora;
    }
    

