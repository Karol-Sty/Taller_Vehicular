    private void B_totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_totalActionPerformed
        try {
        int horasTrabajadas = Integer.parseInt(fecha2.getText());
        String tipoVehiculo = tipo.getText().trim();

        int costoPorHora = 0;

        if ("Automóvil".equals(tipoVehiculo)) {
            costoPorHora = COSTO_AUTOMOVIL;
        } else if ("Camioneta".equals(tipoVehiculo)) {
            costoPorHora = COSTO_CAMIONETA;
        }

        int costoManoDeObra = horasTrabajadas * costoPorHora;

        DefaultTableModel model = (DefaultTableModel) repuestos.getModel();

        int costoRepuestos = 0;
        for (int i = 0; i < model.getRowCount(); i++) {
            Object value = model.getValueAt(i, 1);

            if (value != null) {
                costoRepuestos += (Integer) value;
            }
        }

        int costoTotal = costoManoDeObra + costoRepuestos;

        total.setText(String.valueOf(costoTotal));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error al calcular los costos. Asegúrate de ingresar valores numéricos válidos.");
    }
    }//GEN-LAST:event_B_totalActionPerformed

